package albumInfoProgram;

public class AlbumInfoProgram {

    public static void main(String[] args) {
        
        AlbumCollection albumCollection = new AlbumCollection();
        
        albumCollection.read("albums.txt");

        System.out.println(albumCollection.sort());  
        
        System.out.println(albumCollection.getArtistDuration("Pink Floyd") + "\n");
                
        System.out.println(albumCollection.getLargestAlbum() + "\n");
        
        System.out.println(albumCollection.getLongestTrack() + "\n");
        
        Playlist playlist = new Playlist(albumCollection);
        playlist.read("playlist.txt");
        System.out.println(playlist.getDuration());
    }
}

